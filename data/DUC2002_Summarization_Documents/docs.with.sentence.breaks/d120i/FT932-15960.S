<s docid="FT932-15960" num="4" wdcount="26"> FT 08 APR 93 / John Major's Year: Major's blue period - A year on from success at the polls, the prime minister's popularity has plunged.</s>

<s docid="FT932-15960" num="5" wdcount="8"> He still has a great deal to prove</s>
</HEADLINE>
<BYLINE>

<s docid="FT932-15960" num="6" wdcount="6"> By MICHAEL CASSELL and IVOR OWEN</s>
</BYLINE>
<TEXT>

<s docid="FT932-15960" num="7" wdcount="16"> ONE year ago tomorrow, Mr John Major surprised everyone but himself by winning the general election.</s>

<s docid="FT932-15960" num="8" wdcount="31"> His honeymoon, after a very personal victory, was short-lived and now he begins the second year of his hard-won term as a deeply unpopular leader who has everything still to prove.</s>

<s docid="FT932-15960" num="9" wdcount="46"> The past 12 months have been hijacked by internal party differences over Europe, by the debacle surrounding UK withdrawal from the exchange rates mechanism of the European Monetary System, and by a continuing, deep recession which has disappointed and alienated many traditional Tory supporters in business.</s>

<s docid="FT932-15960" num="10" wdcount="16"> Mr Major's anniversary inevitably led yesterday to an outbreak of political point-scoring over the government's record.</s>

<s docid="FT932-15960" num="11" wdcount="27"> While Mr Tony Newton, leader of the Commons, conceded: 'No one pretends that the last year has been easy,' he also claimed that much had been achieved.</s>

<s docid="FT932-15960" num="12" wdcount="15"> The government's efforts to secure a framework for long-term prosperity was beginning to pay off.</s>

<s docid="FT932-15960" num="13" wdcount="26"> Mr John Smith, the Labour leader, was joined by 10 frontbench colleagues in launching a counter-attack, involving a detailed and documented indictment of Mr Major's failures.</s>

<s docid="FT932-15960" num="14" wdcount="11"> He attacked the Tories for building 'a bonfire of pre-election commitments'.</s>

<s docid="FT932-15960" num="15" wdcount="25"> Tory allegiances in the business world have also been put under immense pressure, though criticism over policy drift and inaction may soften as recession ends.</s>

<s docid="FT932-15960" num="16" wdcount="31"> Yesterday Sir Michael Angus, president of the Confederation of British Industry, remained cautious but volunteered: 'If I were a betting man I would say we have the embryo of a recovery'.</s>

<s docid="FT932-15960" num="17" wdcount="42"> Mr Major can take little comfort from the conclusion, widespread among industrialists, that his greatest achievement in the past year has been Britain's withdrawal from the ERM - something which he resisted and over which, in the end, he had no control.</s>

<s docid="FT932-15960" num="18" wdcount="28"> Most industrialists, however, find it hard to criticise Mr Major as an individual, with many drawing a flattering parallel between his personal qualities and those of his predecessor.</s>

<s docid="FT932-15960" num="19" wdcount="25"> He still attracts gratitude for the election victory, which was seen - despite subsequent problems for the economy - as the best result for business.</s>

<s docid="FT932-15960" num="20" wdcount="25"> But, unlike the previous No 10 occupant, he is seen as a weak leader, with little vision and bereft of a clearly articulated political conviction.</s>

<s docid="FT932-15960" num="21" wdcount="36"> There is a general perception that, important though Britain's future place in Europe is, the prime minister has been excessively preoccupied with community affairs and associated internal party ructions, to the detriment of the domestic economy.</s>

<s docid="FT932-15960" num="22" wdcount="20"> Many industrialists are pleased that Mr Major is now attempting to reshape economic strategy in favour of the manufacturing base.</s>

<s docid="FT932-15960" num="23" wdcount="15"> But the jury is out on whether the rhetoric will give way to effective action.</s>

